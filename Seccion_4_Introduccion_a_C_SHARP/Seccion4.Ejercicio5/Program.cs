// 4.5 - Recorre los números del 1 al 100. Usa un bucle for, imprimirlos por consola

using System;

namespace Seccion4.Ejercicio5
{
    class Program
    {
        static void Main(string[] args)
        {
            for (int i = 1; i <= 100; i++)
            {
                Console.WriteLine(i);
            }
            Console.ReadKey();
        }
    }
}